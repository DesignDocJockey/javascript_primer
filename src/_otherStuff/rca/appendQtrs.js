    while (endingPoint > startingPoint ) {
        const y = startingPoint.format('YY');
        const quarters = getQuartersForYear(y);
        periods.push(...quarters);
        startingPoint.add(1, 'year');
    }

    //const quarters = getQuartersForYear(startingYear);
