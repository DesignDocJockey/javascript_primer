const convertQuarterToMoment = (quarterFormat) => {
    const year = `20${quarterFormat.trim().slice(4).substring(0, 2)}`;
    const mmDD = Moment(quarterFormat, 'Q').format('MM-DD');
    const strDate = `${year}-${mmDD}`;
    return Moment(strDate).utc();
};

